-- --------------------------------------------------------------------------
-- 1.3 Esquema: SEGURIDAD
-- --------------------------------------------------------------------------

CREATE TABLE seguridad.rol_bd (
    id_rol_bd INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre_rol_bd VARCHAR(100) NOT NULL UNIQUE,
    descripcion TEXT,
    activo BOOLEAN DEFAULT TRUE
);

CREATE TABLE seguridad.rol (
    id_rol INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR(100) NOT NULL UNIQUE,
    id_estado INTEGER,
    id_rol_bd INTEGER REFERENCES seguridad.rol_bd(id_rol_bd)
);

CREATE TABLE seguridad.usuario (
    id_usuario INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    correo VARCHAR(150) NOT NULL UNIQUE,
    contrasena VARCHAR(255) NOT NULL,
    id_estado INTEGER NOT NULL,
    fecha_creacion TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    fecha_actualizacion TIMESTAMP
);

CREATE TABLE seguridad.usuario_rol (
    id_usuario_rol INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_usuario INTEGER NOT NULL REFERENCES seguridad.usuario(id_usuario) ON DELETE CASCADE,
    id_rol INTEGER NOT NULL REFERENCES seguridad.rol(id_rol)
);

CREATE TABLE seguridad.tipo_objeto_seguridad (
    id_tipo_objeto INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR(100) NOT NULL,
    descripcion TEXT,
    activo BOOLEAN DEFAULT TRUE
);

CREATE TABLE seguridad.privilegio (
    id_privilegio INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR(150) NOT NULL,
    accion VARCHAR(100),
    activo BOOLEAN DEFAULT TRUE,
    id_tipo_objeto INTEGER REFERENCES seguridad.tipo_objeto_seguridad(id_tipo_objeto),
    esquema VARCHAR(100),
    nombre_tabla VARCHAR(100)
);

CREATE TABLE seguridad.rol_privilegio (
    id_rol_privilegio INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_rol INTEGER NOT NULL REFERENCES seguridad.rol(id_rol) ON DELETE CASCADE,
    id_privilegio INTEGER NOT NULL REFERENCES seguridad.privilegio(id_privilegio) ON DELETE CASCADE
);

CREATE TABLE seguridad.historial_sesion (
    id_sesion INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ip_acceso VARCHAR(50),
    fecha_ingreso TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    fecha_salida TIMESTAMP,
    rol_utilizado VARCHAR(100),
    id_usuario INTEGER NOT NULL REFERENCES seguridad.usuario(id_usuario)
);

CREATE TABLE seguridad.auditoria (
    id_auditoria INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tabla_afectada VARCHAR(100),
    operacion VARCHAR(50),
    descripcion TEXT,
    fecha_hora TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    id_usuario INTEGER NOT NULL REFERENCES seguridad.usuario(id_usuario)
);

CREATE TABLE seguridad.solicitud_registro (
    id_solicitud INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    correo VARCHAR(150) NOT NULL,
    nombres VARCHAR(150) NOT NULL,
    apellidos VARCHAR(150) NOT NULL,
    cedula VARCHAR(20) NOT NULL,
    telefono VARCHAR(30),
    departamento VARCHAR(100),
    cargo VARCHAR(100),
    fecha_solicitud TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    id_estado INTEGER DEFAULT 1,
    motivo_rechazo TEXT,
    procesado_por INTEGER REFERENCES seguridad.usuario(id_usuario),
    fecha_procesamiento TIMESTAMP
);

