-- --------------------------------------------------------------------------
-- 1.8 Esquema: IA_ALERTAS
-- --------------------------------------------------------------------------

CREATE TABLE ia_alertas.modelo_ia (
    id_modelo INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre_modelo VARCHAR(200) NOT NULL,
    version VARCHAR(50),
    descripcion TEXT,
    activo BOOLEAN DEFAULT TRUE
);

CREATE TABLE ia_alertas.ejecucion_ia (
    id_ejecucion INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    parametros_enviados TEXT,
    fecha_ejecucion TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    id_estado INTEGER,
    id_modelo INTEGER NOT NULL REFERENCES ia_alertas.modelo_ia(id_modelo)
);

CREATE TABLE ia_alertas.temporadas_agricolas (
    id_temporada INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre_temporada VARCHAR(150) NOT NULL,
    cultivo VARCHAR(150),
    fecha_inicio DATE,
    fecha_fin DATE,
    id_estado INTEGER
);

CREATE TABLE ia_alertas.alertas_caducidad (
    id_alerta INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    mensaje TEXT,
    fecha_generacion TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    id_estado INTEGER,
    id_lote INTEGER NOT NULL REFERENCES inventario.lotes(id_lote),
    id_nivel_alerta INTEGER NOT NULL REFERENCES catalogos.cat_nivel_alerta(id_nivel_alerta)
);

CREATE TABLE ia_alertas.configuracion_alertas (
    id_configuracion INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    dias_anticipacion INTEGER,
    activo BOOLEAN DEFAULT TRUE,
    id_nivel_alerta INTEGER NOT NULL REFERENCES catalogos.cat_nivel_alerta(id_nivel_alerta),
    id_usuario_modificador INTEGER NOT NULL REFERENCES seguridad.usuario(id_usuario)
);

CREATE TABLE ia_alertas.notificacion (
    id_notificacion INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    canal VARCHAR(50),
    fecha_envio TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    leida BOOLEAN DEFAULT FALSE,
    fecha_lectura TIMESTAMP,
    id_alerta INTEGER NOT NULL REFERENCES ia_alertas.alertas_caducidad(id_alerta),
    id_usuario_destino INTEGER NOT NULL REFERENCES seguridad.usuario(id_usuario)
);

CREATE TABLE ia_alertas.sugerencias_ia (
    id_sugerencia INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    porcentaje_descuento DECIMAL(5,2),
    observaciones TEXT,
    fecha_sugerencia TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    id_lote INTEGER NOT NULL REFERENCES inventario.lotes(id_lote),
    id_temporada INTEGER NOT NULL REFERENCES ia_alertas.temporadas_agricolas(id_temporada),
    id_ejecucion INTEGER NOT NULL REFERENCES ia_alertas.ejecucion_ia(id_ejecucion),
    id_estado_aprobacion INTEGER NOT NULL REFERENCES catalogos.cat_estado_aprobacion(id_estado_aprobacion)
);

CREATE TABLE ia_alertas.promociones (
    id_promocion INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre_promocion VARCHAR(200) NOT NULL,
    descripcion TEXT,
    descuento_global DECIMAL(5,2),
    fecha_inicio DATE,
    fecha_fin DATE,
    id_estado INTEGER,
    id_sugerencia INTEGER REFERENCES ia_alertas.sugerencias_ia(id_sugerencia),
    id_usuario_aprueba INTEGER NOT NULL REFERENCES seguridad.usuario(id_usuario)
);

CREATE TABLE ia_alertas.promocion_detalle (
    id_detalle INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    cantidad_requerida INTEGER DEFAULT 1,
    id_promocion INTEGER NOT NULL REFERENCES ia_alertas.promociones(id_promocion),
    id_producto INTEGER NOT NULL REFERENCES inventario.producto(id_producto)
);

CREATE TABLE ia_alertas.regla_negocio_ia (
    id_regla INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    descuento_maximo DECIMAL(5,2),
    activar_promociones BOOLEAN DEFAULT TRUE,
    activo BOOLEAN DEFAULT TRUE
);

