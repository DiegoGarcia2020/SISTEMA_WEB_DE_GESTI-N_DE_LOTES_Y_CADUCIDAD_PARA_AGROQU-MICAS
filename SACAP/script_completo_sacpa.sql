-- --------------------------------------------------------------------------
-- 1.6 Esquema: INVENTARIO
-- --------------------------------------------------------------------------

CREATE TABLE inventario.categoria (
    id_categoria INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR(150) NOT NULL,
    id_estado INTEGER
);

CREATE TABLE inventario.producto (
    id_producto INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR(200) NOT NULL,
    descripcion TEXT,
    unidad_medida VARCHAR(50),
    precio DECIMAL(10,2),
    id_estado INTEGER,
    id_categoria INTEGER NOT NULL REFERENCES inventario.categoria(id_categoria)
);

CREATE TABLE inventario.registro_sanitario (
    id_registro INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    numero_registro VARCHAR(100),
    organismo_emisor VARCHAR(200),
    fecha_emision DATE,
    fecha_vigencia DATE,
    id_estado INTEGER,
    id_producto INTEGER NOT NULL REFERENCES inventario.producto(id_producto)
);

CREATE TABLE inventario.almacen (
    id_almacen INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR(200) NOT NULL,
    capacidad_total DECIMAL(12,2),
    id_estado INTEGER,
    id_ciudad INTEGER NOT NULL REFERENCES geografia.ciudad(id_ciudad)
);

CREATE TABLE inventario.zona_almacen (
    id_zona INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR(150) NOT NULL,
    condicion_climatica VARCHAR(100),
    id_estado INTEGER,
    id_almacen INTEGER NOT NULL REFERENCES inventario.almacen(id_almacen)
);

CREATE TABLE inventario.estanteria (
    id_estanteria INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    codigo VARCHAR(50) NOT NULL,
    id_estado INTEGER,
    id_zona INTEGER NOT NULL REFERENCES inventario.zona_almacen(id_zona)
);

CREATE TABLE inventario.ubicacion_interna (
    id_ubicacion INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nivel VARCHAR(50),
    posicion VARCHAR(50),
    id_estado INTEGER,
    id_estanteria INTEGER NOT NULL REFERENCES inventario.estanteria(id_estanteria)
);

CREATE TABLE inventario.lotes (
    id_lote INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    numero_lote VARCHAR(100) NOT NULL UNIQUE,
    fecha_fabricacion DATE,
    fecha_vencimiento DATE NOT NULL,
    cantidad_inicial INTEGER,
    cantidad_actual INTEGER,
    fecha_ingreso TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    id_estado_lote INTEGER,
    id_producto INTEGER NOT NULL REFERENCES inventario.producto(id_producto),
    id_proveedor INTEGER NOT NULL REFERENCES entidades.proveedor(id_proveedor),
    id_ubicacion INTEGER NOT NULL REFERENCES inventario.ubicacion_interna(id_ubicacion)
);

CREATE TABLE inventario.documentos_lote (
    id_documento INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre_archivo VARCHAR(200),
    ruta_archivo VARCHAR(500),
    tipo_documento VARCHAR(100),
    fecha_subida TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    id_lote INTEGER NOT NULL REFERENCES inventario.lotes(id_lote)
);

CREATE TABLE inventario.historial_estado_lote (
    id_historial INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_estado_anterior INTEGER,
    id_estado_nuevo INTEGER,
    observacion TEXT,
    fecha_cambio TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    id_lote INTEGER NOT NULL REFERENCES inventario.lotes(id_lote),
    id_usuario INTEGER NOT NULL REFERENCES seguridad.usuario(id_usuario)
);

CREATE TABLE inventario.bodeguero (
    id_bodeguero INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    cedula VARCHAR(20),
    nombres VARCHAR(150),
    apellidos VARCHAR(150),
    telefono VARCHAR(30),
    turno VARCHAR(50),
    id_estado INTEGER,
    id_usuario INTEGER REFERENCES seguridad.usuario(id_usuario)
);

CREATE TABLE inventario.supervisor (
    id_supervisor INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    cedula VARCHAR(20),
    nombres VARCHAR(150),
    apellidos VARCHAR(150),
    telefono VARCHAR(30),
    area_supervision VARCHAR(150),
    id_estado INTEGER,
    id_usuario INTEGER REFERENCES seguridad.usuario(id_usuario)
);

