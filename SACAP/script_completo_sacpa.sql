-- 1.2 Esquema: GEOGRAFIA
-- --------------------------------------------------------------------------

CREATE TABLE geografia.pais (
    id_pais INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR(150) NOT NULL,
    codigo_iso VARCHAR(10)
);

CREATE TABLE geografia.provincia (
    id_provincia INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombre VARCHAR(150) NOT NULL,
    id_pais INTEGER NOT NULL REFERENCES geografia.pais(id_pais)
);

CREATE TABLE geografia.ciudad (
    id_ciudad INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
