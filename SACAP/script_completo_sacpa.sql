    id_usuario INTEGER NOT NULL REFERENCES seguridad.usuario(id_usuario)
);

CREATE TABLE operaciones.devoluciones (
    id_devolucion INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    motivo TEXT,
    cantidad INTEGER,
    fecha_devolucion TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    id_estado_aprobacion INTEGER,
    id_lote INTEGER NOT NULL REFERENCES inventario.lotes(id_lote),
