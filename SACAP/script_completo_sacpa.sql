CREATE TABLE operaciones.uso_campo (
    id_uso INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    parcela VARCHAR(150),
    cultivo VARCHAR(150),
    fecha_aplicacion DATE,
    cantidad_usada INTEGER,
    observacion TEXT,
    id_estado INTEGER,
